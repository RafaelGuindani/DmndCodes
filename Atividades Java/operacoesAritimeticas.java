public class operacoesAritimeticas {
    public static void main(String[] args){
        int a = 35;
        int b = 25;
        int soma = a + b;
        int multiplicacao = a * b;
        int divisao = a / b;
        int subtracao = a - b;

        System.out.println("A soma dos valores "+ a +" e "+ b +" é: "+ soma);
        System.out.println("A multiplicação dos valores "+ a +" e "+ b +" é: "+ multiplicacao);
        System.out.println("A divisão dos valores "+ a +" e "+ b +" é: "+ divisao);
        System.out.println("A subtração dos valores "+ a +" e "+ b +" é: "+ subtracao);


    }}